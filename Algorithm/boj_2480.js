// 📘 문제 번호: 2480

// 🧠 function 이름: boj_2480
// 💡 설명:
// 서로 다른 눈이 적힌 주사위 세 개를 굴렸을 때, 나오는 눈에 따라 다음과 같이 상금을 계산합니다:

// 세 눈이 모두 같다면: 10,000 × 눈

// 두 눈이 같다면: 1,000 × 같은 눈 + 100

// 모두 다르다면: 100 × (가장 큰 눈)
// 세 개의 주사위 눈이 정수로 주어질 때, 상금을 계산하는 프로그램을 작성하세요.

// ✅ 입력 형식

function boj_2480(a, b, c) {
  if (a === b && b === c) {
    return 10000 * a;
  } else if (a === b || a === c) {
    return 1000 * a + 100;
  } else if (b === c) {
    return 1000 * b + 100;
  } else {
    return Math.max(a, b, c) * 100;
  }
}

console.log(boj_2480(3, 3, 6)); // 두 눈이 같은 경우
console.log(boj_2480(2, 2, 2)); // 세 눈이 모두 같은 경우
console.log(boj_2480(6, 2, 5)); // 모두 다른 경우
console.log(boj_2480(4, 4, 1)); // 두 눈이 같은 경우
console.log(boj_2480(1, 3, 1)); // 두 눈이 같은 경우
