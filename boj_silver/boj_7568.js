// ✅ 문제 설명 (요약)
// N명의 사람들의 몸무게와 키가 주어진다.
// 어떤 사람보다 몸무게와 키가 모두 큰 사람이 많을수록 등수가 밀린다.
// 모든 사람의 덩치 등수를 계산하여 출력하라.

// 자신보다 몸무게, 키 둘 다 큰 사람의 수가 곧 등수에서 자신보다 앞선 사람 수다.

// 즉, 덩치 등수 = (자신보다 덩치가 큰 사람 수) + 1

function boj_7568(arr) {
  const result = [];

  for (let i = 0; i < arr.length; i++) {
    // 기준 사람 i
    let count = 1; // 등수는 1부터 시작 (자기 자신 포함)

    for (let j = 0; j < arr.length; j++) {
      // 비교 대상 사람 j
      if (i !== j) {
        // 자기 자신(i와 j가 같으면) 제외
        // 덩치 비교 (몸무게와 키 모두 비교)
        if (arr[j][0] > arr[i][0] && arr[j][1] > arr[i][1]) {
          count++; // j가 i보다 덩치 크면 count 증가
        }
      }
    }

    result.push(count); // i번째 사람의 등수를 결과 배열에 추가
  }

  return result;
}

console.log(
  boj_7568([
    [55, 185],
    [58, 183],
    [88, 186],
    [60, 175],
    [46, 155],
  ])
);
